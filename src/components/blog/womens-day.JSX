import React, { useEffect, useRef, useState } from "react";

export default function WomensDay() {
  // State for comments and commenter info
  const [comments, setComments] = useState([]);
  const [comment, setComment] = useState("");
  const [name, setName] = useState("");
  const [email, setEmail] = useState("");
  const [website, setWebsite] = useState("");
  const [saveInfo, setSaveInfo] = useState(false);
  const [success, setSuccess] = useState(false);

  const LOCAL_STORAGE_KEY = "womensDayComments";
  const COMMENTER_INFO_KEY = "womensDayCommenterInfo";
  const commentInputRef = useRef(null);

  // Load comments and commenter info on mount
  useEffect(() => {
    // Load comments
    const saved = localStorage.getItem(LOCAL_STORAGE_KEY);
    if (saved) {
      try {
        setComments(JSON.parse(saved));
      } catch {
        setComments([]);
      }
    }
    // Load commenter info
    const savedInfo = localStorage.getItem(COMMENTER_INFO_KEY);
    if (savedInfo) {
      try {
        const info = JSON.parse(savedInfo);
        setName(info.name || "");
        setEmail(info.email || "");
        setWebsite(info.website || "");
        setSaveInfo(true);
      } catch {
        // ignore
      }
    }
  }, []);

  // Save comments to localStorage when changed
  useEffect(() => {
    localStorage.setItem(LOCAL_STORAGE_KEY, JSON.stringify(comments));
  }, [comments]);

  // Save commenter info if saveInfo is checked
  useEffect(() => {
    if (saveInfo) {
      localStorage.setItem(
        COMMENTER_INFO_KEY,
        JSON.stringify({ name, email, website })
      );
    }
  }, [saveInfo, name, email, website]);

  // Escape HTML to prevent XSS
  function escapeHtml(text) {
    const div = document.createElement("div");
    div.textContent = text;
    return div.innerHTML;
  }

  // Handle form submit
  function handleSubmit(e) {
    e.preventDefault();
    if (!comment.trim() || !name.trim()) {
      alert("Please fill in the required fields (Comment and Name).");
      return;
    }
    const newComment = {
      name: name.trim(),
      date: new Date().toLocaleString("en-IN", {
        year: "numeric",
        month: "long",
        day: "numeric",
        hour: "2-digit",
        minute: "2-digit",
      }),
      text: comment.trim(),
    };
    setComments((prev) => [...prev, newComment]);
    if (saveInfo) {
      localStorage.setItem(
        COMMENTER_INFO_KEY,
        JSON.stringify({ name, email, website })
      );
    } else {
      localStorage.removeItem(COMMENTER_INFO_KEY);
    }
    setComment("");
    setSuccess(true);
    setTimeout(() => setSuccess(false), 2000);
    if (commentInputRef.current) {
      commentInputRef.current.focus();
    }
  }

  return (
    <div className="bg-gray-100 min-h-screen text-gray-800">
      {/* Header Section */}
      <header className="py-6 text-center" style={{ backgroundColor: "#11616f" }}>
        <h1 className="text-4xl font-bold text-white">
          Women’s Day
        </h1>
      </header>

      {/* Content Section */}
      <main className="max-w-5xl mx-auto p-6 space-y-8">
        <section>
          <h2 className="text-2xl font-semibold mb-3" style={{ color: "#11616f" }}>
            Celebrating Women’s Day
          </h2>
          <p>
            Newashoke Helping Foundation, a leading <span className="font-bold">non-profit organization</span> committed to women empowerment, is set to celebrate International Women’s Day in Gunagan under the guidance of its Managing Trustee “Anudeep Raut”. With a strong commitment to advancing gender equality and eliminating sexism, this celebration promises to be a beacon of positivity and inspiration.
          </p>
        </section>

        <section>
          <h3 className="text-xl font-semibold mb-2" style={{ color: "#11616f" }}>
            The Historical Journey
          </h3>
          <p>
            The roots of <span className="font-bold">International Women’s Day</span> can be traced back to the early 20th century, celebrating women’s groups advocating for better working conditions, rights, and gender equality. The first International Women’s Day focused on the plight and demands of women workers. Since then, it has evolved into a global event that recognizes the achievements of women in all walks of life.
          </p>
        </section>

        <section>
          <h3 className="text-xl font-semibold mb-2" style={{ color: "#11616f" }}>
            Celebrating Diversity and Inclusion
          </h3>
          <p>
            Women’s Day is not only about acknowledging the achievements of a few, but also about recognizing the diversity of experiences and challenges faced by women worldwide. It’s about celebrating women of all races, ethnicities, religions, sexual orientations, and socioeconomic backgrounds. It’s a day to amplify the voices of marginalized women and advocate for their rights.
          </p>
        </section>

        <section>
          <h3 className="text-xl font-semibold mb-2" style={{ color: "#11616f" }}>
            Empowering Women: A Collective Responsibility
          </h3>
          <p>
            Genuine progress and equality isn’t only the responsibility of women themselves but a collective effort involving governments, organizations, communities, and individuals. It requires dismantling systemic barriers, challenging stereotypes, promoting gender equality in education and employment, and ensuring access to healthcare and reproductive rights. Empowered women not only transform their own lives but also contribute to building stronger and more inclusive societies.
          </p>
        </section>

        <section>
          <h3 className="text-xl font-semibold mb-2" style={{ color: "#11616f" }}>
            Inspiring Stories of Resilience
          </h3>
          <p>
            On this Women’s Day, let’s draw inspiration from the countless stories of women who have overcome adversity and made significant contributions to their communities and the world. From activists fighting for women’s equality to scientists breaking barriers in male-dominated fields, from entrepreneurs shattering glass ceilings to mothers nurturing future leaders, women continue to inspire us with their resilience, courage, and determination.
          </p>
        </section>

        <section>
          <h3 className="text-xl font-semibold mb-2" style={{ color: "#11616f" }}>
            Call to Action
          </h3>
          <p>
            As we celebrate Women’s Day, let’s not forget that there is still much work to be done to achieve true gender equality. Let’s pledge to challenge discrimination and violence against women, to build women’s empowerment, and to create a more inclusive and equitable global society for future generations. Let’s commit to celebrate and uplift dignity, equality, and freedom. Happy Women’s Day!
          </p>
        </section>

        {/* Comments Section */}
        <section className="bg-white rounded-lg shadow-md p-6">
          <h3 className="text-xl font-semibold mb-4" style={{ color: "#11616f" }}>
            Comments
          </h3>

          {/* Comment Form */}
          <form className="space-y-4 mb-6" onSubmit={handleSubmit} autoComplete="off">
            <div>
              <label htmlFor="comment" className="block text-sm font-medium text-gray-700 mb-1">
                Comment *
              </label>
              <textarea
                id="comment"
                name="comment"
                rows={4}
                required
                ref={commentInputRef}
                className="w-full px-3 py-2 border border-gray-300 rounded-md focus:outline-none focus:ring-2 focus:ring-red-500 focus:border-transparent"
                placeholder="Share your thoughts about Women's Day..."
                value={comment}
                onChange={(e) => setComment(e.target.value)}
              />
            </div>

            <div className="grid grid-cols-1 md:grid-cols-2 gap-4">
              <div>
                <label htmlFor="name" className="block text-sm font-medium text-gray-700 mb-1">
                  Name *
                </label>
                <input
                  type="text"
                  id="name"
                  name="name"
                  required
                  className="w-full px-3 py-2 border border-gray-300 rounded-md focus:outline-none focus:ring-2 focus:ring-red-500 focus:border-transparent"
                  placeholder="Your name"
                  value={name}
                  onChange={(e) => setName(e.target.value)}
                />
              </div>

              <div>
                <label htmlFor="email" className="block text-sm font-medium text-gray-700 mb-1">
                  Email
                </label>
                <input
                  type="email"
                  id="email"
                  name="email"
                  className="w-full px-3 py-2 border border-gray-300 rounded-md focus:outline-none focus:ring-2 focus:ring-red-500 focus:border-transparent"
                  placeholder="your.email@example.com"
                  value={email}
                  onChange={(e) => setEmail(e.target.value)}
                />
              </div>
            </div>

            <div>
              <label htmlFor="website" className="block text-sm font-medium text-gray-700 mb-1">
                Website
              </label>
              <input
                type="url"
                id="website"
                name="website"
                className="w-full px-3 py-2 border border-gray-300 rounded-md focus:outline-none focus:ring-2 focus:ring-red-500 focus:border-transparent"
                placeholder="https://yourwebsite.com"
                value={website}
                onChange={(e) => setWebsite(e.target.value)}
              />
            </div>

            <div className="flex items-center">
              <input
                type="checkbox"
                id="saveInfo"
                name="saveInfo"
                className="h-4 w-4 text-red-600 focus:ring-red-500 border-gray-300 rounded"
                checked={saveInfo}
                onChange={(e) => setSaveInfo(e.target.checked)}
              />
              <label htmlFor="saveInfo" className="ml-2 block text-sm text-gray-700">
                Save my name, email, and website for next time
              </label>
            </div>

            <button
              type="submit"
              className={`bg-[#11616f] text-white px-6 py-2 rounded-md hover:bg-orange-600 transition-colors duration-200 font-medium ${
                success ? "bg-green-500" : ""
              }`}
              disabled={success}
            >
              {success ? "Comment Posted!" : "Post Comment"}
            </button>
          </form>

          {/* Comments Display */}
          <div className="space-y-4">
            {comments.length === 0 ? (
              <div className="text-gray-500 text-center py-8">
                No comments yet. Be the first to share your thoughts!
              </div>
            ) : (
              comments.map((c, idx) => (
                <div
                  key={idx}
                  className="border-l-4 border-red-500 pl-4 py-3 bg-gray-50 rounded-r-md"
                >
                  <div className="flex justify-between items-start mb-2">
                    <h4 className="font-semibold text-gray-800">
                      {escapeHtml(c.name)}
                    </h4>
                    <span className="text-sm text-gray-500">{c.date}</span>
                  </div>
                  <p className="text-gray-700 leading-relaxed">
                    {escapeHtml(c.text)}
                  </p>
                </div>
              ))
            )}
          </div>
        </section>
      </main>
    </div>
  );
}
